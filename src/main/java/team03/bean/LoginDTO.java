package team03.bean;

public class LoginDTO {
	
	private String id;
	private String pw;
	private String kid;
	
	public void setId(String id) {
		this.id = id;
	}
	public void setPw(String pw) {
		this.pw = pw;	
	}
	public void setKid(String kid) {
		this.kid = kid;
	}
	
	public String getId() {
		return id;
	}
	public String getPw() {
		return pw;
	}
	public String getKid() {
		return kid;
	}



}
