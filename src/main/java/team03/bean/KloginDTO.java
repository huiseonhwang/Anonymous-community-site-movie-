package team03.bean;

public class KloginDTO {
	private String kid;
	private String name;
	private String email;
	
	
	
	public void setKid(String kid) {
		this.kid = kid;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setEmail(String email) {
		this.email=email;
	}
	
	
	public String getKid() {
		return kid;
	}
	public String getName() {
		return name;
	}
	public String getEmail() {
		return email;
	}
}
